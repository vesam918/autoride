class Autoride_ThemeAlign
{
	/**************************************************************************/
	
	public $align;
	
	/**************************************************************************/
	
	function __construct()
	{
		$this->align=array
		(
			'left'=>__('Left','autoride'),
			'center'=>__('Center','autoride'),
			'right'=>__('Right','autoride')
		);
	}
	
	/**************************************************************************/
	
	function getAlign()
	{
		return($this->align);
	}
	
	/**************************************************************************/
	
	function isAlign($align)
	{
		return(array_key_exists($align,$this->align));
	}
	
	/**************************************************************************/
}

