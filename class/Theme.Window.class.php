class Autoride_ThemeWindow
{
	/**************************************************************************/
	
	public $target;
	
	/**************************************************************************/
	
	function __construct()
	{		
		$this->target=array
		(	
			'_self'=>array(__('Open page in the same frame','autoride')),
			'_blank'=>array(__('Open page in a new window','autoride'))
		);		
	}
	
	/**************************************************************************/
	
	function getTarget()
	{
		return($this->target);
	}
	
	/**************************************************************************/
	
	function isTarget($name)
	{
		return(array_key_exists($name,$this->target));
	}
	
	/**************************************************************************/
}

