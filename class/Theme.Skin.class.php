class Autoride_ThemeSkin
{
	/**************************************************************************/
	
	public $skin;
	
	/**************************************************************************/
	
	function __construct()
	{
		$this->skin=array
		(
			'general'=>array
			(
				'landing_url'=>'http://quanticalabs.com/wp_themes4/autoride/'
			),
			'skin'=>array
			(
				1=>array
				(
					'name'=>__('Default','autoride')
				)
			)
		);
	}
	
	/**************************************************************************/
	
	function getSkinDictionary()
	{
		return($this->skin);
	}

	/**************************************************************************/
	
	function getSkin()
	{
		return(get_option(AUTORIDE_THEME_SKIN_OPTION_NAME,1));
	}
	
	/**************************************************************************/
}

