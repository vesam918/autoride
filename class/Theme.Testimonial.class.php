class Autoride_ThemeTestimonial
{
	/**************************************************************************/
	
	public $style;
	
	/**************************************************************************/
	
	function __construct()
	{
		$this->style=array
		(
			'1'=>__('Style 1','autoride'),
			'2'=>__('Style 2','autoride'),
		);
	}
	
	/**************************************************************************/
	
	function getStyle()
	{
		return($this->style);
	}
	
	/**************************************************************************/
	
	function isStyle($style)
	{
		return(array_key_exists($style,$this->getStyle()) ? true : false);
	}   

	/**************************************************************************/
}

