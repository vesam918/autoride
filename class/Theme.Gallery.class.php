class Autoride_ThemeGallery
{
	/**************************************************************************/
	
	public $itemWidth;
	
	/**************************************************************************/
	
	function __construct()
	{	
		$this->itemWidth=array
		(
			'25'=>__('25%','autoride'),
			'50'=>__('50%','autoride'),
		);
	}
	
	/**************************************************************************/
	
	function getItemWidth()
	{
		return($this->itemWidth);
	}
	
	/**************************************************************************/
	
	function isItemWidth($width)
	{
		return(array_key_exists($width,$this->itemWidth));
	}

	/**************************************************************************/
}

