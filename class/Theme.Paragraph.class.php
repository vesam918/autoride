class Autoride_ThemeParagraph
{
	/**************************************************************************/
	
	public $style;
	
	/**************************************************************************/
	
	function __construct()
	{		
		$this->style=array
		(
			'1'=>array(__('Style 1','autoride')),
			'2'=>array(__('Style 2','autoride'))
		);	
	}
	
	/**************************************************************************/
	
	function getStyle()
	{
		return($this->style);
	}
	
	/**************************************************************************/
	
	function isStyle($style)
	{
		return(array_key_exists($style,$this->style));
	}
	
	/**************************************************************************/
}

