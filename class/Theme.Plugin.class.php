class Autoride_ThemePlugin
{
	/**************************************************************************/

	function __construct() 
	{ 

	}
	
	/**************************************************************************/
	
	static function isActive($className)
	{
		return(class_exists($className));
	}
	
	/**************************************************************************/
	
	static function doShortcode($className,$shortcode)
	{
		if(!self::isActive($className)) return;
		return(do_shortcode($shortcode));
	}

	/**************************************************************************/
}

