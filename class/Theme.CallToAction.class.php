class Autoride_ThemeCallToAction
{
	/**************************************************************************/

	public $style;
	
	/**************************************************************************/
	
	function __construct()
	{	
		$this->style=array
		(
			'1'=>array(__('Style 1','autoride')),
			'2'=>array(__('Style 2','autoride'))
		);
	}
	
	/**************************************************************************/
	
	function getStyle()
	{
		return($this->style);
	}
	
	/**************************************************************************/
	
	function isStyle($name)
	{
		return(array_key_exists($name,$this->getStyle()));
	}

	/**************************************************************************/
}

