class Autoride_ThemeFeatureCircle
{
	/**************************************************************************/
	
	public $labelPosition;
	
	/**************************************************************************/
	
	function __construct()
	{	
		$this->labelPosition=array
		(
			'top'=>__('Top','autoride'),
			'right'=>__('Right','autoride'),
			'bottom'=>__('Bottom','autoride'),
			'left'=>__('Left','autoride')
		);
	}
	
	/**************************************************************************/
	
	function getLabelPosition()
	{
		return($this->labelPosition);
	}
	
	/**************************************************************************/
	
	function isLabelPosition($position)
	{
		return(array_key_exists($position,$this->labelPosition));
	}

	/**************************************************************************/
}

